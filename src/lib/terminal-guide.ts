export function getTerminalGuide(terminalNm: string): TerminalGuide | null {
  return terminalGuideMap[terminalNm] || null;
}

/**
 * 주요 터미널 꿀팁 (노선 페이지에서 출발/도착 터미널 이름으로 간단 팁 제공)
 */
export function getTerminalTip(terminalNm: string): string | null {
  const guide = terminalGuideMap[terminalNm];
  if (!guide) return null;

  const parts: string[] = [];
  if (guide.transport.subway) parts.push(guide.transport.subway);
  else if (guide.transport.bus) parts.push(guide.transport.bus);
  if (guide.parking?.info) parts.push(`주차: ${guide.parking.info.split('.')[0]}`);

  return parts.length > 0 ? parts.join('. ') : null;
}
